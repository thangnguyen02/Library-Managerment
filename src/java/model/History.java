package model;

/**
 *
 * @author acer
 */
public class History {
    private String id;
    private String name;
    private String date;
    private String status;
    private String note;

    public History() {
    }

    public History(String id, String name, String date, String status, String note) {
        this.id = id;
        this.name = name;
        this.date = date;
        this.status = status;
        this.note = note;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
    
}
